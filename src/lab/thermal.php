font-family:"Times New Roman","serif"'> </span><span style='font-family:"Times New Roman","serif";
mso-ansi-language:EN-US'>Thermal lens (TL) spectroscopy is based on the
photo-thermal effect where energy of a photon, fully or partially, is converted
into heat energy. In this spectroscopy, a laser source is used whose wavelength
is chosen such that it can couple either with electronic levels or vibration
levels of the sample under study. Beside this, energy can be transferred to the
sample by scattering but energy transferred to sample, generally, negligible
small. So we can neglect this mode of energy transfer mechanism. Energy
transferred in the former case, leads the system in the excited state. From
there, system can relax to the ground through various mechanism such as <span
class=SpellE>radiative</span> (fluorescence and phosphorescence) or non-<span
class=SpellE>radiative</span> mechanism. If system relax via non-<span
class=SpellE>radiative</span> mechanism, it completely transfer its absorbed
energy<span style='mso-spacerun:yes'>  </span>to the system in the form of heat
while in case of <span class=SpellE>radiative</span> relaxation, some amount of
energy is converted in the form of heat due the stokes shift. Because of all
these heating mechanism, a spot of local heating is formed. As refractive index
of medium is depend on the density and density changes with the change of
temperature. All these factors make the local heats to be having smaller
refractive index compare to the surrounding. Thermal conduction and convection
try to equilibrate the heat generated in the sample and as we move away from
the local heated area temperature decreases. In corollary to this, refractive
index increases and system behave as Lens. As this lens is formed due to the
heating (Thermal) in sample, so it is called Thermal Lens (TL). Also, it is
based on light matter interaction, so it is called Thermal lens Spectroscopy. <o:p></o:p></span></p>

